/**
 * add - function to sum two input integers
 *
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the sum of @a and @b
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function to subtract
 *	    one input integer from the other
 *
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the subtraction of @b from @a
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function to multiply two input integers
 *
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the product of @a and @b
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function to divide two input integers
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the division of @a by @b
*/

int div(int a, int b)
{
	return (a / b);

}

/**
 * mod - function to get the remainder
 *	    of the division between two input integers
 *
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the remainder of the division
*/

int mod(int a, int b)
{
	return (a % b);
}
