/**
 * op_add - function to sum two input integers
 *
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the sum of @a and @b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract
 *	    one input integer from the other
 *
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the subtraction of @b from @a
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply two input integers
 *
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the product of @a and @b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide two input integers
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the division of @a by @b
*/

int op_div(int a, int b)
{
	return (a / b);

}

/**
 * op_mod - function to get the remainder
 *	    of the division between two input integers
 *
 * @a: first input integer
 * @b: second input integer
 *
 * Return: return the remainder of the division
*/

int op_mod(int a, int b)
{
	return (a % b);
}
